function toDigits(puzzle) {
    let result = {},
             i = 0;

    // TODO: I almost lost my mind reconciling the coordinates used here
    //       versus those used in the solver.  Consider making them the same.
    [0, 1, 2].forEach(column => {
        [0, 1, 2].forEach(depth => {
            [2, 1, 0].forEach(row => {
                if (puzzle[i] !== '.') {
                    result[[row, column, depth]] = Number(puzzle[i]);
                }
                ++i;
            })
        })
    });

    return result;
}

function difficulty(digits) {
    const numClues = Object.keys(digits).length;

    return {
        10: 2,
        9:  3,
        8:  4
    }[numClues] || 1;
}

